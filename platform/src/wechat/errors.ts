// 微信公众号 API 错误码 -> 中文可读提示。
// 官方错误码全集见 https://developers.weixin.qq.com/doc/offiaccount/Getting_Started/Global_Return_Code.html
// 这里只精选内容发布链路（取 token / 上传素材 / 建草稿）最常撞到的码，
// 其余未知码统一兜底，避免把一长串无关码塞进运营看的报错里。
const WX_ERROR_MESSAGES: Record<number, string> = {
  // 成功（一般不会走到 explain，但保留以防上层把 0 也传进来）
  0: '请求成功',
  // 系统/限流类
  '-1': '微信系统繁忙，请稍后重试',
  45009: '接口调用超过频率限制（45009），请降低调用频率或稍后重试',
  // token 类
  40001: 'access_token 无效或已过期（40001），通常是 appid/secret 不匹配，或 token 被其它进程刷新覆盖',
  40014: 'access_token 不合法（40014），请确认传入的是 access_token 本身而非其它凭证',
  42001: 'access_token 已超时（42001），需要重新获取',
  // 配置/权限类
  40013: 'AppID 不合法（40013），请检查公众号 appid 是否填写正确',
  40125: 'AppSecret 不正确（40125），请检查公众号 secret',
  40164: '调用方 IP 不在公众号 IP 白名单内（40164），请在公众号后台「基本配置」加入服务器出口 IP',
  // 内容/素材类
  45003: '标题长度超出限制（45003），公众号图文标题最长 64 字，请精简标题',
  45007: '语音播放时间超出限制（45007）',
  45008: '图文消息超出数量限制（45008）',
  53404: '账号已被限制发布能力（53404），请检查公众号是否被封禁或处于受限状态',
  53500: '发布功能被封禁（53500）',
  // 素材上传类
  40004: '不合法的媒体文件类型（40004），请确认上传的是支持的图片格式（jpg/png）',
  40007: '不合法的媒体文件 id（40007）',
  41005: '缺少媒体文件数据（41005），上传时未带上 media 字段或文件为空',
}

/**
 * 把微信返回的 errcode 翻译成给运营看的中文提示。
 * 未知码返回带原始码的通用提示，方便排查时去官方文档查。
 */
export function explainWxError(errcode: number): string {
  const known = WX_ERROR_MESSAGES[errcode]
  if (known) return known
  return `微信接口返回未知错误码 ${errcode}，请对照官方「全局返回码说明」排查`
}
